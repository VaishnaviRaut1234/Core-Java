package com.polymorphism;

public class NumberOperations 
{
	public void addNumbers(int a,int b,float c)
	{
		System.out.println("Addition of three numbers are : "+(a+b+c));
	}
	public void addNumbers(int a,int b)
	{
		System.out.println("Addition of two integer numbers are : "+(a+b));
	}
	public void addNumbers(int a,float c)
	{
		System.out.println("Addition of two integer and float numbers are : "+(a+c));
	}
}
