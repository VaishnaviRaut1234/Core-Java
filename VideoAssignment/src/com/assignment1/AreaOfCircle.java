//Write a java program to calculate area of circle.

package com.assignment1;

public class AreaOfCircle 
{

	public static void main(String[] args) 
	{
		float radius = 5f;
		float pi = 3.14f;
		
		float area = pi*radius*radius;
		
		System.out.println("Area of Circle is "+area);

	}

}
